public class Usuario {
    private String alias;
    
    public Usuario(){
        
    }
    
    public Usuario(String alias){
        this.alias = alias;
    }
    

    /**
     * @return the alias
     */
    public String getAlias() {
        return alias;
    }

    /**
     * @param alias the alias to set
     */
    public void setAlias(String alias) {
        this.alias = alias;
    }
}
